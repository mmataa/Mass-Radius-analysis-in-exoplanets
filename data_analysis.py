# Definimos función para calcular ajuste
def ajuste(x,y):
  sum_1 = 0
  sum_2 = 0
  for i in range(len(x)):
    sum_1 += (x[i] - np.mean(x))*(y[i] - np.mean(y))
    sum_2 += (x[i] - np.mean(x))**2

  b1 = sum_1/sum_2
  b0 = np.mean(y) - b1*np.mean(x)
  return b1, b0

# Definimos función para evaluar ajuste
def calidad_ajuste(x, y, b0, b1):
  stcc = 0
  sce = 0

  for i in range(len(x)):
    stcc += (y[i] - np.mean(y))**2
    sce += (y[i] - b0 - (b1*x[i]))**2
  return 1 - (sce/stcc)

def sigma_clipping(x, y, b0, b1, param):
  # Aplicamos sigma clipping
  clipped_x = np.array([])
  clipped_y = np.array([])
  residuos = np.array([])
  for i in range(len(y)):
    residuos = np.append(residuos, y[i] - b0 - (b1*x[i]))

  for i in range(len(x)):
    if abs(y[i] - b0 - (b1*x[i])) < param*np.std(residuos):
      clipped_y = np.append(clipped_y, y[i])
      clipped_x = np.append(clipped_x, x[i])

  return clipped_x, clipped_y

def clasificacion_regiones(lim_inf, lim_sup, x, y):
  clase1_masa = np.array([])
  clase1_radio = np.array([])
  clase2_masa = np.array([])
  clase2_radio = np.array([])
  clase3_masa = np.array([])
  clase3_radio = np.array([])
  for i in range(len(x)):
      if x[i] < lim_inf:
          clase1_masa = np.append(clase1_masa, x[i])
          clase1_radio = np.append(clase1_radio, y[i])
      elif lim_inf < x[i] < lim_sup:
          clase2_masa = np.append(clase2_masa, x[i])
          clase2_radio = np.append(clase2_radio, y[i])
      else:
          clase3_masa = np.append(clase3_masa, x[i])
          clase3_radio = np.append(clase3_radio, y[i])

  return clase1_masa, clase1_radio, clase2_masa, clase2_radio, clase3_masa, clase3_radio

def interseccion(b0_2, b1_2, b0_1, b1_1):
    x_interseccion = (b0_2 - b0_1) / (b1_1 - b1_2)
    return x_interseccion

