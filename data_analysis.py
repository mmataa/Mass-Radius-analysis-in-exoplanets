# Definimos función para evaluar ajuste
def calidad_ajuste(x, y, b0, b1):
  stcc = 0
  sce = 0

  for i in range(len(x)):
    stcc += (y[i] - np.mean(y))**2
    sce += (y[i] - b0 - (b1*x[i]))**2
  return 1 - (sce/stcc)

